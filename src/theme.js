//  React context for the color mode
//  This is the function that will allow us to change the color
// Defines a context that provides a function which toggle shte color mode between dark and light
export const ColorModeContext = createContext({
  toggleColorMode: () => {},
});

// The useMode hook is defined to manage the current color mode and theme.
// It uses the useState and useMemo hooks from React. The hook returns an array containing the theme and colorMode
export const useMode = () => {
  const [mode, setMode] = useState("dark");

  const colorMode = useMemo(
    () => ({
      toggleColorMode: () =>
        setMode((prev) => (prev === "light" ? "dark" : "light")),
    }),
    []
  );

  const theme = useMemo(() => createTheme(themeSettings(mode)), [mode]);

  return [theme, colorMode];
};

